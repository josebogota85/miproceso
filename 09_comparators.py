 #Los operadores de comparación son operadores que nos sirven como su nombre lo indica para comparar dos o mas valores, esta comparación generalmente nos produce, True o False como resultado
print(7>3)# En este ejemplo nos va a dar un resultado True, debido a que 7 , si es mayor a 3
print(3>7)# En este caso dará False por que 3 no es mayor a 7
print(7>7)#En este caso también dará False por que 7 no es mayor que 7
